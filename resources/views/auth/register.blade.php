        $(document).ready(function() {
            $("#city").autocomplete({
                source: function(request, response) {
                    $.ajax({
                        url: "{{ route('autocomplete.cities') }}",
                        dataType: "json",
                        data: {
                            term: request.term
                        },
                        success: function(data) {
                            response(data);
                        }
                    });
                },
                minLength: 2,
                select: function(event, ui) {
                    $("#city").val(ui.item.value);
                }
            });
        });
    </script>

